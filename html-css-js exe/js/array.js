let fruits = ["Pine", "Banana", "Mango"];
fruits.pop(); //Remove the last element "Mango"
console.log(fruits); //Pine,Banana

//Append element at the end
let fruitss = ["Pine","Banana"];
fruitss.push("Mango"); //add element at the end of the array
console.log(fruitss);

//Methods to remove element from the beginning of the array:
let fruitsz = ["Pine","Apple","Mango"];
fruits.shift();
console.log(fruitsz); //Pine, Apple

//Add element at the beginning of the array
let fruit = ["Pine", "Mango"];
fruit.unshift("Banana");
console.log(fruit); //Banana, Pine, Mango